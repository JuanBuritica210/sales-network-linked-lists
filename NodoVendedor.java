package Vendedores;


public class NodoVendedor {
	
	 private Vendedor vendedor;
	    private NodoVendedor next;

	    public Vendedor getVendedor() {
	        return vendedor;
	    }

	    public void setVendedor(Vendedor vendedor) {
	        this.setVendedor(vendedor);
	    }

	    public NodoVendedor getNext() {
	        return next;
	    }

	    public void setNext(NodoVendedor next) {
	        this.next = next;
	    }
	
}
